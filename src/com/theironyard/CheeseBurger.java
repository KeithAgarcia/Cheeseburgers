public class CheeseBurger {
    String mayo;
    int patties;
    int buns;
    int bacon;
    String instructions;

    public CheeseBurger(String mayo, int patties, int buns, int bacon, String instructions) {
        this.mayo = mayo;
        this.patties = patties;
        this.buns = buns;
        this.bacon = bacon;
        this.instructions = instructions;
    }
}
